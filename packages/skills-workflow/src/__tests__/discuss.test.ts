// ---------------------------------------------------------------------------
// Tests
// ---------------------------------------------------------------------------

describe('discussSkill', () => {
  const mockedReadFile = vi.mocked(readFile);
  const mockedResolvePhaseDir = vi.mocked(resolvePhaseDir);
  const mockedWritePhaseArtifact = vi.mocked(writePhaseArtifact);
  const mockedParseRoadmap = vi.mocked(parseRoadmap);
  const mockedParseStateMd = vi.mocked(parseStateMd);

  beforeEach(() => {
    vi.clearAllMocks();

    // Default mocks for normal flow
    mockedReadFile.mockImplementation((path: any) => {
      const pathStr = String(path);
      if (pathStr.includes('STATE.md')) {
        return Promise.resolve(MOCK_STATE_CONTENT);
      }
      if (pathStr.includes('ROADMAP.md')) {
        return Promise.resolve(MOCK_ROADMAP_CONTENT);
      }
      return Promise.reject(new Error(`Unexpected readFile: ${pathStr}`));
    });

    mockedResolvePhaseDir.mockResolvedValue('/test/project/.planning/phases/05-context-planning');
    mockedWritePhaseArtifact.mockResolvedValue('/test/project/.planning/phases/05-context-planning/05-CONTEXT.md');

    mockedParseStateMd.mockReturnValue({
      phase: 5,
      plan: '1 of 5',
      status: 'executing',
      lastActivity: '2026-03-28',
      progress: {
        totalPhases: 10,
        completedPhases: 4,
        totalPlans: 35,
        completedPlans: 30,
        percent: 0,
      },
    });

    mockedParseRoadmap.mockReturnValue({
      phases: [
        {
          number: 5,
          name: 'Context Planning',
          description: 'Extract design decisions through agent-guided conversation',
          completed: false,
          requirements: ['WF-09', 'WF-10'],
          plans: [],
          planCount: 5,
          completedCount: 0,
        },
      ],
      progress: [],
    });
  });

  // -------------------------------------------------------------------------
  // Metadata
  // -------------------------------------------------------------------------

  it('has correct skill metadata', () => {
    expect(discussSkill.id).toBe('workflow.discuss');
    expect(discussSkill.command).toBe('discuss');
    expect(discussSkill.kind).toBe('prompt');
    expect(discussSkill.stage).toBe('stable');
    expect(discussSkill.category).toBe('workflow');
    expect(discussSkill.routing).toBe('routable');
  });

  // -------------------------------------------------------------------------
  // No provider
  // -------------------------------------------------------------------------

  it('returns failure when no AI provider is available', async () => {
    const ctx = createMockContext({
      agent: {
        run: vi.fn(),
        crossVerify: vi.fn(),
        listProviders: vi.fn().mockResolvedValue([]),
      } as unknown as SkillContext['agent'],
    });

    const result = await discussSkill.execute(ctx);

    expect(result.success).toBe(false);
    expect(result.summary).toMatch(/no ai provider/i);
  });

  // -------------------------------------------------------------------------
  // Missing ROADMAP
  // -------------------------------------------------------------------------

  it('returns failure when ROADMAP.md is not found', async () => {
    mockedReadFile.mockImplementation((path: any) => {
      const pathStr = String(path);
      if (pathStr.includes('STATE.md')) {
        return Promise.resolve(MOCK_STATE_CONTENT);
      }
      return Promise.reject(new Error('ENOENT'));
    });

    const ctx = createMockContext();
    const result = await discussSkill.execute(ctx);

    expect(result.success).toBe(false);
    expect(result.summary).toMatch(/roadmap/i);
  });

  // -------------------------------------------------------------------------
  // Normal flow
  // -------------------------------------------------------------------------

  it('executes full flow: analyze -> ask -> deep-dive -> scenarios -> write CONTEXT.md', async () => {
    let agentCallCount = 0;
    const ctx = createMockContext({
      agent: {
        run: vi.fn().mockImplementation(() => {
          agentCallCount++;
          if (agentCallCount === 1) {
            // Analyze: return gray areas
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_GRAY_AREAS_OUTPUT }));
          }
          if (agentCallCount <= 3) {
            // Deep-dive: return decision for each gray area
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_DEEPDIVE_OUTPUT }));
          }
          // Scenario generation
          return Promise.resolve(createMockAgentResult({ outputText: MOCK_SCENARIO_OUTPUT }));
        }),
        crossVerify: vi.fn().mockResolvedValue([]),
        listProviders: vi.fn().mockResolvedValue(['claude-code-cli']),
      } as unknown as SkillContext['agent'],
    });

    const result = await discussSkill.execute(ctx);

    expect(result.success).toBe(true);

    // Should have asked user about gray areas (2 gray areas)
    expect(ctx.ui.ask).toHaveBeenCalledTimes(2);

    // Should have written CONTEXT.md
    expect(mockedWritePhaseArtifact).toHaveBeenCalled();
    const writeCall = mockedWritePhaseArtifact.mock.calls[0];
    expect(writeCall[0]).toBe('/test/project');
    expect(writeCall[1]).toBe(5);
    expect(String(writeCall[3])).toContain('CONTEXT.md');

    // CONTEXT.md content should contain required sections
    const contextContent = String(writeCall[4]);
    expect(contextContent).toContain('<domain>');
    expect(contextContent).toContain('<decisions>');
    expect(contextContent).toContain('<canonical_refs>');
    expect(contextContent).toContain('<code_context>');
    expect(contextContent).toContain('<specifics>');
    expect(contextContent).toContain('<deferred>');

    // Should have written scenarios
    expect(ctx.fileStore.write).toHaveBeenCalled();
  });

  // -------------------------------------------------------------------------
  // Agent parse failure fallback
  // -------------------------------------------------------------------------

  it('falls back to text question when agent output cannot be parsed as gray areas', async () => {
    let agentCallCount = 0;
    const ctx = createMockContext({
      agent: {
        run: vi.fn().mockImplementation(() => {
          agentCallCount++;
          if (agentCallCount === 1) {
            // Return non-parseable output
            return Promise.resolve(createMockAgentResult({
              outputText: 'Here are some thoughts about gray areas without proper JSON format...',
            }));
          }
          if (agentCallCount === 2) {
            // Deep-dive for single text area
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_DEEPDIVE_OUTPUT }));
          }
          // Scenario generation
          return Promise.resolve(createMockAgentResult({ outputText: MOCK_SCENARIO_OUTPUT }));
        }),
        crossVerify: vi.fn().mockResolvedValue([]),
        listProviders: vi.fn().mockResolvedValue(['claude-code-cli']),
      } as unknown as SkillContext['agent'],
    });

    const result = await discussSkill.execute(ctx);

    // Should still succeed -- fallback to askText
    expect(result.success).toBe(true);
    // Should have used askText fallback (not ask with options)
    expect(ctx.ui.askText).toHaveBeenCalled();
  });

  // -------------------------------------------------------------------------
  // Scenario generation failure
  // -------------------------------------------------------------------------

  it('still writes CONTEXT.md when scenario generation fails', async () => {
    let agentCallCount = 0;
    const ctx = createMockContext({
      agent: {
        run: vi.fn().mockImplementation(() => {
          agentCallCount++;
          if (agentCallCount === 1) {
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_GRAY_AREAS_OUTPUT }));
          }
          if (agentCallCount <= 3) {
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_DEEPDIVE_OUTPUT }));
          }
          // Scenario generation fails
          return Promise.reject(new Error('Agent timeout'));
        }),
        crossVerify: vi.fn().mockResolvedValue([]),
        listProviders: vi.fn().mockResolvedValue(['claude-code-cli']),
      } as unknown as SkillContext['agent'],
    });

    const result = await discussSkill.execute(ctx);

    // Should still succeed (partial failure is OK)
    expect(result.success).toBe(true);

    // CONTEXT.md should still be written
    expect(mockedWritePhaseArtifact).toHaveBeenCalled();

    // Scenarios should NOT have been written (they failed)
    expect(ctx.fileStore.write).not.toHaveBeenCalled();

    // Should have warnings
    expect(result.warnings).toBeDefined();
    expect(result.warnings!.length).toBeGreaterThan(0);
  });

  // -------------------------------------------------------------------------
  // Phase override via args
  // -------------------------------------------------------------------------

  it('uses --phase override when provided', async () => {
    let agentCallCount = 0;
    const ctx = createMockContext({
      args: { phase: 3 },
      agent: {
        run: vi.fn().mockImplementation(() => {
          agentCallCount++;
          if (agentCallCount === 1) {
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_GRAY_AREAS_OUTPUT }));
          }
          if (agentCallCount <= 3) {
            return Promise.resolve(createMockAgentResult({ outputText: MOCK_DEEPDIVE_OUTPUT }));
          }
          return Promise.resolve(createMockAgentResult({ outputText: MOCK_SCENARIO_OUTPUT }));
        }),
        crossVerify: vi.fn().mockResolvedValue([]),
        listProviders: vi.fn().mockResolvedValue(['claude-code-cli']),
      } as unknown as SkillContext['agent'],
    });

    mockedParseRoadmap.mockReturnValue({
      phases: [
        {
          number: 3,
          name: 'Standalone TS Skills',
          description: 'Build standalone TypeScript skills',
          completed: false,
          requirements: ['SK-01'],
          plans: [],
          planCount: 6,
          completedCount: 0,
        },
      ],
      progress: [],
    });

    mockedResolvePhaseDir.mockResolvedValue('/test/project/.planning/phases/03-standalone-ts-skills');

    await discussSkill.execute(ctx);

    // Should use phase 3 for writePhaseArtifact, not 5 from STATE.md
    const writeCall = mockedWritePhaseArtifact.mock.calls[0];
    expect(writeCall[1]).toBe(3);
  });
});
