/**
 * Tests for discuss.skill.ts
 *
 * Verifies:
 * - Skill metadata (id, command, kind, stage, category, routing)
 * - No-provider fallback (graceful error)
 * - Missing ROADMAP.md error
 * - Normal flow: analyze -> ask -> deep-dive -> scenarios -> write CONTEXT.md
 * - Agent parse failure fallback (non-JSON gray areas)
 * - Partial failure: scenario generation fails, CONTEXT.md still written
 */

import { describe, it, expect, vi, beforeEach } from 'vitest';

// ---------------------------------------------------------------------------
// Mocks (must be before imports)
// ---------------------------------------------------------------------------

vi.mock('node:fs/promises', () => ({
  readFile: vi.fn(),
}));

vi.mock('../shared/phase-reader.js', () => ({
  resolvePhaseDir: vi.fn(),
  readPhaseArtifact: vi.fn(),
  writePhaseArtifact: vi.fn(),
}));

vi.mock('../shared/roadmap-parser.js', () => ({
  parseRoadmap: vi.fn(),
}));

vi.mock('../shared/state-reader.js', () => ({
  parseStateMd: vi.fn(),
}));

import { readFile } from 'node:fs/promises';
import { resolvePhaseDir, writePhaseArtifact } from '../shared/phase-reader.js';
import { parseRoadmap } from '../shared/roadmap-parser.js';
import { parseStateMd } from '../shared/state-reader.js';
import discussSkill from '../discuss.skill.js';
import type { SkillContext } from '@sunco/core';
import type { AgentResult } from '@sunco/core';

// ---------------------------------------------------------------------------
// Fixtures
// ---------------------------------------------------------------------------

