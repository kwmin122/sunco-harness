/**
 * Build a prompt for creating new plans via /ultraplan (--draft mode).
 * Used when no PLAN.md files exist yet.
 */
export function buildUltraplanDraftPrompt(params: UltraplanDraftParams): string {
  const {
    contextMd,
    researchMd,
    requirementsMd,
    roadmapMd,
    phaseGoal,
    requirements,
    phaseSlug,
    paddedPhase,
  } = params;

  return `# Ultraplan Draft: Phase ${paddedPhase} — ${phaseSlug}

## Phase Goal
${phaseGoal}

## Requirements to Cover
${requirements.map((r) => `- ${r}`).join('\n')}

## Input Documents

### CONTEXT.md
${contextMd}

### RESEARCH.md
${researchMd || '(No research available)'}

### REQUIREMENTS.md
${requirementsMd || '(No requirements file available)'}

### ROADMAP.md
${roadmapMd || '(No roadmap available)'}

## Instructions

Create 2-5 execution plans for this phase. Each plan should have 2-3 tasks.

${PLAN_FORMAT_SPEC}
`.trim();
}

// ---------------------------------------------------------------------------
// Output parser (import mode: ultraplan result → PLAN.md strings)
// ---------------------------------------------------------------------------

/** Separator used between plans in ultraplan output */
const PLAN_SEPARATOR = '---PLAN_SEPARATOR---';

/**
 * Parse ultraplan output into individual PLAN.md content strings.
 *
 * Only accepts structured output:
 * - Case A: Output uses ---PLAN_SEPARATOR--- markers → split and return
 * - Case B: Multiple frontmatter-delimited plans (--- ... --- blocks)
 * - Case C: Single plan with frontmatter + XML tags
 *
 * Unstructured markdown (e.g., "looks good" summaries) returns empty array
 * to prevent accidental overwrite of existing plans.
 */
export function parseUltraplanOutput(
  content: string,
  _phaseNumber: number,
  _phaseSlug: string,
): string[] {
  const trimmed = content.trim();
  if (!trimmed) return [];

  // Case A: Has PLAN_SEPARATOR markers
  if (trimmed.includes(PLAN_SEPARATOR)) {
    return trimmed
      .split(PLAN_SEPARATOR)
      .map((s) => s.trim())
      .filter((s) => s.length > 0);
  }

  // Case B: Multiple frontmatter-delimited plans
  const fmBlocks = splitByFrontmatter(trimmed);
  if (fmBlocks.length > 0) {
    return fmBlocks;
  }

  // Case C: Single plan with frontmatter + XML objective tag
  if (trimmed.includes('---\n') && trimmed.includes('<objective>')) {
    return [trimmed];
  }

  // Unstructured content — reject to prevent accidental overwrite
  return [];
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

/**
 * Split content that contains multiple frontmatter-delimited plans.
 * Looks for patterns like: --- ... --- ... <objective> that repeat.
 */
function splitByFrontmatter(content: string): string[] {
  // Match each plan: starts with --- (frontmatter open), has content, ends before next ---\n
  const planRegex = /(?:^|\n)(---\n[\s\S]*?\n---[\s\S]*?)(?=\n---\n|$)/g;
  const plans: string[] = [];
  let match: RegExpExecArray | null;

  while ((match = planRegex.exec(content)) !== null) {
    const plan = match[1].trim();
    // Only include if it looks like a real plan (has frontmatter + some content)
    if (plan.startsWith('---') && plan.length > 50) {
      plans.push(plan);
    }
  }

  // Only return if we found multiple plans; single plan handled by caller
  return plans.length > 1 ? plans : [];
}

